package eva1_5_variables;
public class EVA1_5_VARIABLES {
    public static void main(String[] args) {
        //Declaracion de Variables
        //Crear las variables para que se puedan usar
        //Tipo de dato / identificador
        String name;
        byte age;
        //Inicialización
        //Usamos el operador = --> OPERADOR DE ASIGNACIÓN
        name = "Jesus Aranda";
        age = 18;
        
        //Uso de Variables
        System.out.println(name);
        System.out.println(age);
        
    }
}
