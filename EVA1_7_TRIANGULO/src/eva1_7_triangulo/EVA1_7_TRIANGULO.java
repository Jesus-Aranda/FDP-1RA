
package eva1_7_triangulo;
public class EVA1_7_TRIANGULO {

    public static void main(String[] args) {
        //Area triangulo
        //Area = (Base * Altura) entre 2;
        
        double area, base, alture;//Tres variables de tipo double
    
        base = 10;
        alture = 8;
        area = (base*alture)/2;
        
        System.out.println("El area del triangulo base 10 y altura 8 es: ");
        System.out.println(area);
        
        base = 11;
        alture = 3;
        area = (base*alture)/2.0;
        
        System.out.println("El area del triangulo base 11 y altura 3 es: ");
        System.out.println(area);
         
        
        
    }
    
}
